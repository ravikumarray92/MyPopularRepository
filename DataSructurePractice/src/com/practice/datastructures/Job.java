package com.practice.datastructures;

public class Job {
	
	int jobId;
	int profit;
	public Job(int jobId, int profit) {
		super();
		this.jobId = jobId;
		this.profit = profit;
	}
	public int getJobId() {
		return jobId;
	}
	public void setJobId(int jobId) {
		this.jobId = jobId;
	}
	public int getProfit() {
		return profit;
	}
	public void setProfit(int profit) {
		this.profit = profit;
	}
	
	

}
		